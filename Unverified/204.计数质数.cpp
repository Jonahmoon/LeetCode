// @lc code=start
class Solution {
public:
    int countPrimes(int n) { // 素数筛算法
        vector<int> isPrime(n, 1);
        int ans = 0;
        for (int i = 2; i < n; ++i) {
            if (isPrime[i]) {
                ans += 1;
                if ((long long)i * i < n) { // 这一步是剪枝操作，满足这一条件的i需要筛的数已经被之前的数筛过了
                    for (int j = i * i; j < n; j += i) {
                        isPrime[j] = 0;
                    }
                }
            }
        }
        return ans;
    }
};
// @lc code=end

